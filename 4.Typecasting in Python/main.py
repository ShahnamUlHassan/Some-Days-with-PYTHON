# Variable 'a' ko integer value 1 assign kiya
a = 1  

# Variable 'b' ko integer value 2 assign kiya
b = 2  

# Variable 'c' mein 'a' aur 'b' ka sum store kiya (1 + 2 = 3)
c = a + b  

# 'c' ka result print kar diya
print(c)  


# Variable 'a' ko string "1" assign kiya
a = "1"  

# Variable 'b' ko string "2" assign kiya
b = "2"  

# int(a) aur int(b) dono ko string se integer mein typecast kiya
# phir unka sum kiya (1 + 2 = 3)
print(int(a) + int(b))  



# Variable 'c' ko ek floating point value 1.9 assign kiya
c = 1.9  

# Variable 'd' ko ek integer value 8 assign kiya
d = 8  

# Python automatically float (1.9) aur int (8) ko add kar deta hai
# Agar ek operand float hai to result bhi float ban jata hai
print(c + d)  
