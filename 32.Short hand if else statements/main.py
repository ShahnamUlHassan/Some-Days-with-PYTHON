# Assign values to variables 'a' and 'b'
a = 330
b = 3303

# Short-hand if-else (Ternary Operator)
# This will print:
# "A" if a > b
# "=" if a == b
# "B" if a < b
print("A") if a > b else print("=") if a == b else print("B")

# Another shorthand if statement
# Will print 9 if a is less than b
print(9) if a < b else ""

# Will print 9 if a is greater than b
print(9) if a > b else ""

